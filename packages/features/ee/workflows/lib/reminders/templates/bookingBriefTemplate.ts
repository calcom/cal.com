import type { BriefingOutput } from "@calcom/features/bookings/lib/generateBriefing";

function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function attendeeInitials(name: string): string {
  const parts = name.trim().split(/\s+/).filter(Boolean);
  const a = parts[0]?.[0] ?? "?";
  const b = parts.length > 1 ? parts[1]?.[0] ?? "" : "";
  return `${a}${b}`.toUpperCase();
}

export function bookingBriefTemplate(params: {
  organizerName: string;
  attendeeName: string;
  eventTitle: string;
  startTime: Date;
  timeZone: string;
  briefing: BriefingOutput;
}): { subject: string; html: string } {
  const { organizerName, attendeeName, eventTitle, startTime, timeZone, briefing } = params;
  const subject = `Booking Brief: ${attendeeName} \u2013 ${eventTitle}`;
  const when = escapeHtml(
    startTime.toLocaleString("en-US", {
      weekday: "long",
      month: "long",
      day: "numeric",
      hour: "numeric",
      minute: "2-digit",
      timeZone,
      timeZoneName: "short",
    })
  );
  const initials = escapeHtml(attendeeInitials(attendeeName));
  const agendaRows = briefing.suggestedAgenda
    .map(
      (item, i) =>
        `<tr><td style="padding:10px 0;border-left:4px solid #6366f1;padding-left:12px"><span style="color:#6366f1;font-weight:600">${i + 1}.</span> ${escapeHtml(item)}</td></tr>`
    )
    .join("");

  const html = `<!DOCTYPE html><html><body style="margin:0;font-family:Helvetica,Arial,sans-serif">
<table width="100%" cellpadding="0" cellspacing="0" style="background:#f3f4f6;padding:24px 12px">
<tr><td align="center">
<table width="600" cellpadding="0" cellspacing="0" style="max-width:600px;width:100%;background:#fff;border-radius:8px;overflow:hidden">
<tr><td style="background-color:#111827;color:#fff;padding:24px 28px">
<h1 style="margin:0 0 8px;font-size:20px;font-weight:600">Booking Brief</h1>
<p style="margin:0;font-size:14px;opacity:0.9">${escapeHtml(attendeeName)} · ${escapeHtml(eventTitle)}</p>
<p style="margin:8px 0 0;font-size:13px;opacity:0.85">${when} · ${escapeHtml(organizerName)}</p>
</td></tr>
<tr><td style="padding:24px 28px">
<table width="100%" cellpadding="0" cellspacing="0" style="border:1px solid #e5e7eb;border-radius:8px">
<tr><td style="padding:16px 18px">
<table cellpadding="0" cellspacing="0"><tr>
<td style="width:44px;height:44px;background:#111827;color:#fff;border-radius:50%;text-align:center;line-height:44px;font-weight:600">${initials}</td>
<td style="padding-left:14px;vertical-align:middle;font-size:15px;font-weight:600;color:#111827">${escapeHtml(attendeeName)}</td>
</tr></table>
<p style="margin:14px 0 0;font-size:14px;color:#374151;line-height:1.5">${escapeHtml(briefing.attendeeSummary)}</p>
</td></tr></table>
<p style="margin:20px 0 8px;font-size:12px;text-transform:uppercase;letter-spacing:0.06em;color:#6b7280">Suggested agenda</p>
<table width="100%" cellpadding="0" cellspacing="0">${agendaRows}</table>
<table width="100%" cellpadding="0" cellspacing="0" style="margin-top:20px;background:#f9fafb;border-left:4px solid #6366f1;border-radius:4px">
<tr><td style="padding:14px 16px;font-size:14px;color:#1f2937;font-style:italic">${escapeHtml(briefing.openingQuestion)}</td></tr></table>
<table width="100%" cellpadding="0" cellspacing="0" style="margin-top:16px;background:#f0fdf4;border-left:4px solid #16a34a;border-radius:4px">
<tr><td style="padding:12px 16px"><span style="font-size:12px;font-weight:600;color:#166534">Prep tip</span>
<p style="margin:6px 0 0;font-size:14px;color:#14532d">${escapeHtml(briefing.prepTip)}</p></td></tr></table>
<p style="margin:24px 0 0;font-size:11px;color:#9ca3af">Generated by Booking Brief · Cal.com</p>
</td></tr></table></td></tr></table></body></html>`;

  return { subject, html };
}
