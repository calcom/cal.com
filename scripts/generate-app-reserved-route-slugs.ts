#!/usr/bin/env ts-node
/**
 * Generates packages/lib/generated/app-reserved-route-slugs.generated.ts
 * by scanning the apps/web filesystem for top-level routes.
 *
 * Uses the same scanning logic as pagesAndRewritePaths.ts (via scan-app-routes.ts)
 * so both next.config.ts and isReservedRouteSlug() derive from the same route set.
 *
 * Usage: yarn generate:app-reserved-slugs
 */
import * as fs from "fs";
import * as path from "path";

import { scanAppRoutes, VIRTUAL_ROUTES } from "../apps/web/scan-app-routes";

const webAppDir = path.resolve(__dirname, "../apps/web");
const outputFile = path.resolve(__dirname, "../packages/lib/generated/app-reserved-route-slugs.generated.ts");

const routesFromFilesystem = scanAppRoutes(webAppDir);

const allSlugs = [...new Set([...routesFromFilesystem, ...VIRTUAL_ROUTES])]
  .map((s) => s.toLowerCase())
  .filter((v, i, self) => self.indexOf(v) === i)
  .sort();

const slugArrayStr = allSlugs.map((s) => `  "${s}",`).join("\n");
const content = `// Auto-generated by 'yarn generate:app-reserved-slugs'. Do not edit manually.
export const APP_RESERVED_ROUTE_SLUGS: string[] = [
${slugArrayStr}
];
`;

fs.writeFileSync(outputFile, content);
console.log(`Generated ${outputFile} with ${allSlugs.length} app reserved route slugs.`);
